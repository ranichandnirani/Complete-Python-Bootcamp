# Variables are used to store data that can be used and manipulated in a program. A variable is created when you assign a value to it using the = operator.

age = 25 #integer 
name = "John" #string 
cgpa = 6.78 #float

# Rules of defining variables:
# 1. Variable names can contain letters, numbers, and underscores.  
# 2. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 3. Variable names are case-sensitive (age and Age are different variables).
# 4. Variable names cannot be a reserved keyword in Python (like if, else, while, etc.).
